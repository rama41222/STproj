    private void searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyReleased
        try{
            String sql = "SELECT * from category where category_id like ? or category_name like ? or sex like ?";
            ps = con.prepareStatement(sql);
            ps.setString(1, "%" + search.getText() + "%");
            ps.setString(2, "%" + search.getText() + "%");
            ps.setString(3, search.getText() + "%");
            rs = ps.executeQuery();
            table_Categories.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(Exception e){
            JOptionPane.showConfirmDialog(null, e);
        }finally{
            
            try{
                rs.close();
                ps.close();
                
            }catch(Exception e){
                JOptionPane.showConfirmDialog(null, e);
            }
        }
        
    }//GEN-LAST:event_searchKeyReleased

