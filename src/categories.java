    private void table_CategoriesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_CategoriesMouseClicked
       try{
       int row = table_Categories.getSelectedRow();
       String index = table_Categories.getModel().getValueAt(row, 0).toString();
       String sql = "SELECT * from category where category_id = '" + index + "'";
       ps = con.prepareStatement(sql);
       rs = ps.executeQuery();
       if(rs.next()){
           String id = rs.getString("category_id");
           String name = rs.getString("category_name");
           String sx = rs.getString("sex");
           cat_id.setText(id);
           cat_name.setText(name);
           gender.setText(sx);
           
       }
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
       }
       
    }//GEN-LAST:event_table_CategoriesMouseClicked

