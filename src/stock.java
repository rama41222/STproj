    private void stock_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_stock_tableMouseClicked
        // TODO add your handling code here:
        
        int row = stock_table.getSelectedRow();
        String index = stock_table.getModel().getValueAt(row, 0).toString();
        String sql = "select * from stock where stock_num= '" + index + "'";
        
        try{
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            if(rs.next())
            {
                    String st = rs.getString("stock_num");
                    String qt = rs.getString("Qty");
                    String min = rs.getString("min_stock");
                    String dis = rs.getString("description");
                    
                    stockNo.setText(st);
                    discription.setText(dis);
                    qty.setText(qt);
                    limit.setText(min);
            }
             
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_stock_tableMouseClicked

