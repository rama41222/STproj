    private void user_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_user_tableMouseClicked
        
               try{
            int row = user_table.getSelectedRow();
            String index = user_table.getModel().getValueAt(row, 0).toString();
            String sql = "select * from user where id ='" + index + "'";
            
        
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            if(rs.next()){
                
                    String pId = rs.getString("id");
                    String nam = rs.getString("name");
                    String usr = rs.getString("username");
                    String pw = rs.getString("password");
                    String ty = rs.getString("type");
                    String is_online = rs.getString("is_online");
                   
                    iD.setText(pId);
                    name.setText(nam);
                    username.setText(usr);
                    password.setText(pw);
                    type.setText(ty);
                    isOnline.setText(is_online);
                    
                    
            }
             
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_user_tableMouseClicked

