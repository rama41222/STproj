    private void order_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_order_tableMouseClicked
        
      
        try{
            int row = order_table.getSelectedRow();
            String index = order_table.getModel().getValueAt(row, 0).toString();
            String sql = "select * from Inventory.order where order_no ='" + index + "'";
            
        
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            if(rs.next()){
                
                    String oN = rs.getString("order_no");
                    String qt = rs.getString("qty");
                    String tC = rs.getString("total_cost");
                    String bP = rs.getString("buying_price");
                    String hC = rs.getString("handling_cost");
                    
                    orderNo.setText(oN);
                    qty.setText(qt);
                    totalCost.setText(tC);
                    buyingPrice.setText(bP);
                    handlingCost.setText(hC);
            }
             
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_order_tableMouseClicked

