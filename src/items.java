    private void items_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_items_tableMouseClicked

        try{
        int row = items_table.getSelectedRow();
        String index = items_table.getModel().getValueAt(row, 0).toString();
        String sql = "SELECT * from item where item_code = '" + index + "'";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            
            if(rs.next()){
                String itemC = rs.getString("item_code");
                String nam = rs.getString("name");
                String bP = rs.getString("buying_price");
                String sP = rs.getString("selling_Price");
                String dis = rs.getString("discription");
                String s = rs.getString("size");
                String cId = rs.getString("category_id"); 
                
                
                itemCode.setText(itemC);
                name.setText(nam);
                buyingPrice.setText(bP);
                sellingPrice.setText(sP);
                categoryId.setText(cId);
                discript.setText(dis);
                size.setText(s);
                
                
                
            }
        }catch(Exception e){
            
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_items_tableMouseClicked

